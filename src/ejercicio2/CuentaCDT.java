package ejercicio2;

public class CuentaCDT {

    private int saldoCDT,tiempoCDT;
    private float interesCDT;

    public CuentaCDT() {
        this.saldoCDT = saldoCDT;
        this.tiempoCDT = tiempoCDT;
        this.interesCDT = interesCDT;
    }

    public double getSaldoCDT() {
        return saldoCDT;
    }

    public void setSaldoCDT(int saldoCDT) {
        this.saldoCDT = saldoCDT;
    }

    public double getTiempoCDT() {
        return tiempoCDT;
    }

    public void setTiempoCDT(int tiempoCDT) {
        this.tiempoCDT = tiempoCDT;
    }

    public float getInteresCDT() {
        return interesCDT;
    }

    public void setInteresCDT(float interesCDT) {
        this.interesCDT = interesCDT;
    }
}
